# == is equal to
print(1 == 2)

# != is not equal to
print(1 != 2)

# <= is less than or equal to
print(1 <= 2)

# < is less than
print(1 < 2)

# >= is greater than or equal to
print(1 >= 2)

# > is greater than
print(1 > 2)

# or either or both could be true
print(1 or 0)
print(1 or 1)

# and both must be true
print(1 and 1)
print(1 and 0)

# not opposite truth value
print(not 1)
print(not 0)